def load_image_paths(input_dir):
    """Loads paths of supported images from the input directory."""
    print(f"Loading image paths from: {input_dir}")
    if not os.path.isdir(input_dir):
        raise ValueError(f"Input directory not found: {input_dir}")
    image_paths = []
    for ext in SUPPORTED_IMAGE_EXTENSIONS:
        pattern = os.path.join(input_dir, f"*{ext}")
        image_paths.extend(glob.glob(pattern))
        pattern_upper = os.path.join(input_dir, f"*{ext.upper()}")
        image_paths.extend(glob.glob(pattern_upper))
    image_paths = sorted(list(set(image_paths)))
    if not image_paths:
        raise ValueError(f"No supported images found in {input_dir}. Supported: {SUPPORTED_IMAGE_EXTENSIONS}")
    print(f"Found {len(image_paths)} image files.")
    return image_paths

"""## 7. Batch Feature Extraction (Faster on GPU)"""

def _extract_dinov2_batch(image_paths, model_name, batch_size):
    """Batch DINOv2 extraction."""
    device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
    model = torch.hub.load('facebookresearch/dinov2', model_name)
    model.eval()
    model.to(device)

    transform = transforms.Compose([
        transforms.Resize(256, interpolation=transforms.InterpolationMode.BICUBIC),
        transforms.CenterCrop(224),
        transforms.ToTensor(),
        transforms.Normalize(mean=[0.485, 0.456, 0.406], std=[0.229, 0.224, 0.225]),
    ])

    all_features = []
    valid_paths = []
    batch_tensors = []
    batch_paths = []

    for img_ref in tqdm(image_paths):
        try:
          if INPUT_MODE == "s3":
              img = open_pil_from_s3(AWS_S3_BUCKET, img_ref)
              rf_input = presigned_url(AWS_S3_BUCKET, img_ref)
              img_for_embed = preprocess_for_embedding(img, rf_input)
          else:
              img = Image.open(img_ref).convert('RGB')
              img_for_embed = preprocess_for_embedding(img, img_ref)

          img_t = transform(img_for_embed)
          batch_tensors.append(img_t)
          batch_paths.append(img_ref)

          if len(batch_tensors) >= batch_size:
              batch = torch.stack(batch_tensors).to(device)
              with torch.no_grad():
                  features = model(batch).cpu().numpy()
              all_features.extend(features)
              valid_paths.extend(batch_paths)
              batch_tensors = []
              batch_paths = []
        except Exception as e:
            print(f"Error processing {img_ref}: {e}")

    if batch_tensors:
        batch = torch.stack(batch_tensors).to(device)
        with torch.no_grad():
            features = model(batch).cpu().numpy()
        all_features.extend(features)
        valid_paths.extend(batch_paths)

    return np.array(all_features), valid_paths

def extract_features_batch(image_paths, method, model_name, batch_size=32):
    """Batch feature extraction."""
    print(f"Extracting features in batches (batch_size={batch_size})...")
    if method == 'dinov2':
        return _extract_dinov2_batch(image_paths, model_name, batch_size)
    else:
        raise ValueError(f"Batch extraction not implemented for {method}")

