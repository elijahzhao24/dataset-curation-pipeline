def list_s3_image_keys(bucket: str, prefix: str):
    paginator = s3.get_paginator("list_objects_v2")
    keys = []
    for page in paginator.paginate(Bucket=bucket, Prefix=prefix):
        for obj in page.get("Contents", []):
            key = obj["Key"]
            if key.endswith("/"):
                continue
            if any(key.lower().endswith(ext) for ext in SUPPORTED_IMAGE_EXTENSIONS):
                keys.append(key)
    keys = sorted(keys)
    if not keys:
        raise ValueError(f"No supported images found in s3://{bucket}/{prefix}")
    return keys

def open_pil_from_s3(bucket: str, key: str) -> PillowImage.Image:
    obj = s3.get_object(Bucket=bucket, Key=key)
    data = obj["Body"].read()
    return PillowImage.open(io.BytesIO(data)).convert("RGB")

def presigned_url(bucket: str, key: str, expires: int = 3600) -> str:
    return s3.generate_presigned_url(
        "get_object",
        Params={"Bucket": bucket, "Key": key},
        ExpiresIn=expires,
    )

def load_image_from_s3(bucket, key):
    obj = s3.get_object(Bucket=bucket, Key=key)
    img_bytes = obj["Body"].read()
    return PillowImage.open(io.BytesIO(img_bytes)).convert("RGB")

def make_s3_key(local_path: str, prefix: str) -> str:
    base = os.path.basename(local_path)
    return prefix.rstrip("/") + "/" + base

def map_input_key_to_output_key(input_key: str) -> str:
    output_prefix = S3_OUTPUT_PREFIX.rstrip("/")
    input_prefix = S3_INPUT_PREFIX.rstrip("/") + "/"

    if input_key.startswith(input_prefix):
        suffix = input_key[len(input_prefix):]
    else:
        suffix = input_key.lstrip("/")

    return f"{output_prefix}/{suffix}"

def copy_s3_object(src_bucket: str, src_key: str, dst_bucket: str, dst_key: str):
    """
    Server-side copy.
    """
    copy_source = {"Bucket": src_bucket, "Key": src_key}
    s3.copy_object(Bucket=dst_bucket, Key=dst_key, CopySource=copy_source)

NEAREST_K_SQL = """
SELECT id, s3_bucket, s3_key, (embedding <=> %s::vector) AS cosine_distance
FROM public.image_vectors
ORDER BY embedding <=> %s::vector
LIMIT %s;
"""
def to_pgvector_literal(x: np.ndarray) -> str:
    x = np.asarray(x, dtype=np.float32).ravel()
    return "[" + ",".join(map(str, x.tolist())) + "]"

def find_nearest(cur, emb_norm: np.ndarray,  k: int = 1):
    v = to_pgvector_literal(emb_norm)
    cur.execute(NEAREST_K_SQL, (v, v, k))
    return cur.fetchall()

INSERT_SQL = """
INSERT INTO public.image_vectors (s3_bucket, s3_key, embedding_version, embedding)
VALUES (%s, %s, %s, %s::vector)
ON CONFLICT (s3_bucket, s3_key)
DO UPDATE SET
  embedding_version = EXCLUDED.embedding_version,
  embedding = EXCLUDED.embedding;
"""

def insert_vector(cur, bucket, key, emb_norm):
    v = to_pgvector_literal(emb_norm)
