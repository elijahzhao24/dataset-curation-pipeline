def farthest_point_sampling(points: np.ndarray, k: int) -> np.ndarray:
    """
    Performs farthest point sampling to select k diverse points from a set of points.

    Args:
        points (np.ndarray): A 2D numpy array of data points.
        k (int): The number of points to sample.

    Returns:
        np.ndarray: An array of indices of the selected k points.
    """
    if k <= 0 or k > len(points):
        raise ValueError("k must be a positive integer")

    # Select a random starting point
    farthest_pts_indices = np.zeros(k, dtype=int)
    farthest_pts_indices[0] = random.randint(0, len(points) - 1)

    # Initialize distances to all points as infinite
    distances = np.full(len(points), np.inf)

    for i in range(1, k):
        # Calculate distances from the last chosen point to all other points
        last_chosen_point = points[farthest_pts_indices[i-1]]
        # Using squared Euclidean distance for efficiency, as we only care about relative magnitude
        new_distances = np.sum((points - last_chosen_point)**2, axis=1)

        # Update minimum distances: for each point, keep the minimum distance to any already selected point
        distances = np.minimum(distances, new_distances)

        # Select the point with the maximum minimum distance (farthest point)
        farthest_pts_indices[i] = np.argmax(distances)

    print(f"Selected {k} points using farthest point sampling.")
    return farthest_pts_indices

