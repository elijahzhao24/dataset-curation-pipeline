    """Downloads specific S3 objects given their s3_uris from selected_ids."""


    base_directory = os.path.join(output_dir, datetime.now().strftime("%Y-%m-%d_%H:%M:%S"))
    for uuid, s3_uri in selected_ids:
        try:
            # Extract the S3 key from the s3_uri
            # Example: s3://filtered-images.dev/raw-images/20260126_133129_434137_rgb.png
            # We need to get 'raw-images/20260126_133129_434137_rgb.png'

            key_parts = s3_uri.split(f"s3://{bucket}/")

            if len(key_parts) < 2:
                print(f"Warning: Could not parse S3 URI: {s3_uri}. Skipping.")
                continue

            key = key_parts[1]

            local_path = os.path.join(base_directory, key)
            os.makedirs(os.path.dirname(local_path), exist_ok=True)

            print(f"Downloading {s3_uri} \u2192 {local_path}")
            s3.download_file(bucket, key, local_path)
        except Exception as e:
            print(f"Error downloading {s3_uri}: {e}")

    print("Download complete.")

