"""Polygon points to binary mask"""

def polygon_area(points):
    pts = np.array([[p["x"], p["y"]] for p in points], dtype=np.float32)
    if len(pts) < 3:
        return 0.0
    x = pts[:, 0]
    y = pts[:, 1]
    return 0.5 * abs(np.dot(x, np.roll(y, 1)) - np.dot(y, np.roll(x, 1)))

def prediction_to_mask(prediction: dict, H: int, W: int) -> np.ndarray:
    """
    Returns boolean mask of shape (H, W)
    """
    mask = np.zeros((H, W), dtype=np.uint8)

    pts = prediction.get("points", None)
    if not pts:
        return mask.astype(bool)

    polygon = np.array([[int(p["x"]), int(p["y"])] for p in pts], dtype=np.int32)

    # clamp points inside image
    polygon[:, 0] = np.clip(polygon[:, 0], 0, W - 1)
    polygon[:, 1] = np.clip(polygon[:, 1], 0, H - 1)

    cv2.fillPoly(mask, [polygon], 1)
    return mask.astype(bool)

def get_best_bin_mask(result: dict, H: int, W: int, class_name: str = "tote-bin"):
    """
    choose highest confidence bin mask
    """
    preds = result.get("predictions", []) or []
    if not preds:
        return None, None  # mask, meta

    # filter by class if present
    filtered = [p for p in preds if p.get("class") == class_name] or preds

    best = max(filtered, key=lambda p: float(p.get("confidence", 0.0)))
    mask = prediction_to_mask(best, H, W)

    meta = {
        "confidence": float(best.get("confidence", 0.0)),
        "class": best.get("class", None),
        "bbox": [
            int(best.get("x", 0) - best.get("width", 0) / 2),
            int(best.get("y", 0) - best.get("height", 0) / 2),
            int(best.get("x", 0) + best.get("width", 0) / 2),
            int(best.get("y", 0) + best.get("height", 0) / 2),
        ],
    }
    return mask, meta

"""Image Cropper"""

def mask_to_bbox(mask: np.ndarray):
    ys, xs = np.where(mask)
    if len(xs) == 0:
        return None
    x0, x1 = xs.min(), xs.max()
    y0, y1 = ys.min(), ys.max()
    return int(x0), int(y0), int(x1) + 1, int(y1) + 1

def crop_mask_pad_square(pil_img: Image.Image, mask: np.ndarray, pad: int = 10, bg: int = 128):
    """
    - crops tightly to mask bbox (+pad)
    - sets outside-mask pixels to bg
    - pads to square with bg
    """
    img = np.array(pil_img.convert("RGB"))
    H, W = img.shape[:2]

    if mask is None or mask.shape != (H, W):
        return None

    bbox = mask_to_bbox(mask)
    if bbox is None:
        return None

    x0, y0, x1, y1 = bbox
    x0 = max(0, x0 - pad); y0 = max(0, y0 - pad)
    x1 = min(W, x1 + pad); y1 = min(H, y1 + pad)

    crop = img[y0:y1, x0:x1].copy()
    crop_mask = mask[y0:y1, x0:x1].astype(bool)

    crop[~crop_mask] = bg

    ch, cw = crop.shape[:2]
    side = max(ch, cw)
    out = np.full((side, side, 3), bg, dtype=np.uint8)

    oy = (side - ch) // 2
    ox = (side - cw) // 2
    out[oy:oy+ch, ox:ox+cw] = crop

    return Image.fromarray(out), (x0, y0, x1, y1)


def preprocess_for_embedding(pil_img: Image.Image, img_path: str) -> Image.Image:
    """
    Returns the image you want to embed.
    - If USE_BIN_MASK_FOR_EMBEDDING: run Roboflow, crop+mask+pad-square
    - Else: return original pil_img
    """
    if not USE_BIN_MASK_FOR_EMBEDDING:
        return pil_img

    img_np = np.array(pil_img)
    H, W = img_np.shape[:2]

    rf_result = roboflow_infer(rf_model, img_np)

    # Convert the PredictionGroup object to a dictionary format expected by get_best_bin_mask
    # The PredictionGroup object has a 'predictions' attribute which is a list of Prediction objects.
    # Each Prediction object typically has a .json() method to convert it to a dict.
    preds_as_dicts = [p.json() for p in rf_result.predictions] if hasattr(rf_result, 'predictions') else []
    rf_result_for_mask = {'predictions': preds_as_dicts}

    mask, _meta = get_best_bin_mask(rf_result_for_mask, H, W, class_name=ROBOFLOW_BIN_CLASS)

    out = crop_mask_pad_square(pil_img, mask, pad=ROBOFLOW_PAD, bg=ROBOFLOW_BG)

    # fallback
    if out is None:
        return pil_img

    roi_pil, _bbox = out
    return roi_pil

